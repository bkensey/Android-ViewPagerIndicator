package com.viewpagerindicator.view;

import android.content.Context;
import android.util.AttributeSet;
import android.widget.Spinner;

/**
 * Created by Brent on 6/15/13.
 */
public class SpinnerView extends Spinner {
    public SpinnerView(Context context) {
        super(context);
    }

    public SpinnerView(Context context, int mode) {
        super(context, mode);
    }

    public SpinnerView(Context context, AttributeSet attrs) {
        super(context, attrs);
    }

    public SpinnerView(Context context, AttributeSet attrs, int defStyle) {
        super(context, attrs, defStyle);
    }

    public SpinnerView(Context context, AttributeSet attrs, int defStyle, int mode) {
        super(context, attrs, defStyle, mode);
    }
}
